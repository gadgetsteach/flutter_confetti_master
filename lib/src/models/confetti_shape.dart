/// The shape of a confetti particle.
enum ConfettiShape {
  /// A simple circle particle.
  circle,

  /// A square/rectangle particle.
  square,

  /// A triangle particle.
  triangle,

  /// A five-pointed star particle.
  star,

  /// A heart-shaped particle.
  heart,

  /// An emoji character particle.
  emoji,

  /// A dollar bill note particle.
  money,

  /// A trophy / medal particle.
  trophy,
}
